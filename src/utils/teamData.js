export function abbrFromName(name) {
  if (!name) return '???'
  if (TEAM_ABBR_MAP[name]) return TEAM_ABBR_MAP[name]
  return name.split(' ').map(w => w[0]).join('').toUpperCase().slice(0, 3)
}

export function teamColor(abbr) {
  return TEAM_COLORS[abbr] || '#000000'
}
